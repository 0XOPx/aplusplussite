let heroTypingTimer = null;
let heroTypedLen    = 0;

function setHeroTab(key) {
  document.querySelectorAll('#heroTabs .tab').forEach(t => {
    t.classList.toggle('active', t.dataset.tab === key);
  });
  const code = HERO_EXAMPLES[key];
  const el   = document.getElementById('heroCode');
  clearInterval(heroTypingTimer);
  heroTypedLen = 0;
  el.innerHTML = '';

  heroTypingTimer = setInterval(() => {
    heroTypedLen = Math.min(heroTypedLen + 4, code.length);
    el.innerHTML  = renderCode(code.slice(0, heroTypedLen));
    if (heroTypedLen >= code.length) clearInterval(heroTypingTimer);
  }, 12);
}

document.getElementById('heroTabs').addEventListener('click', e => {
  const btn = e.target.closest('.tab');
  if (btn) setHeroTab(btn.dataset.tab);
});

setHeroTab('hello');

