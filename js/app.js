// ── Syntax Highlighter ───────────────────────────────────────────────────────

const APP_KEYWORDS = new Set([
  'func','let','const','var','if','else','elif','while','for','in','return',
  'class','self','true','false','null','println','print','input','str','int',
  'float','push','pop','len','throw','try','catch','finally','and','or','not',
  'match','case','default','break','continue','import','new','delete','async',
  'await','yield','typeof','sizeof','as','is','namespace','use',
]);
const APP_TYPES = new Set(['int','float','double','string','bool','char','void','list','map','set','tuple','enum']);

const LANG_KEYWORDS = {
  cpp:    new Set(['auto','int','return','cout','endl','include','using','namespace','std','for','if','void','class']),
  python: new Set(['def','return','if','while','print','import','for','in','elif','else','and','or','not','True','False','None','class','self']),
  csharp: new Set(['static','void','int','return','class','using','for','new','string','if','var','bool','Console']),
  js:     new Set(['function','return','if','while','let','const','var','console','log','for','of','in','new','class','this']),
};

function tokenizeLine(line, lang) {
  const kws  = lang === 'app' ? APP_KEYWORDS   : (LANG_KEYWORDS[lang] || new Set());
  const typs = lang === 'app' ? APP_TYPES       : new Set();
  const tokens = [];
  let i = 0;

  while (i < line.length) {
    // Comment
    if ((line[i] === '/' && line[i+1] === '/') || line[i] === '#') {
      tokens.push({ cls: 'tok-comment', text: line.slice(i) });
      break;
    }
    // String (double quote)
    if (line[i] === '"' || line[i] === '`') {
      const q = line[i]; let j = i + 1;
      while (j < line.length && line[j] !== q) { if (line[j] === '\\') j++; j++; }
      tokens.push({ cls: 'tok-string', text: line.slice(i, j + 1) });
      i = j + 1; continue;
    }
    // String (single quote)
    if (line[i] === "'") {
      let j = i + 1;
      while (j < line.length && line[j] !== "'") { if (line[j] === '\\') j++; j++; }
      tokens.push({ cls: 'tok-string', text: line.slice(i, j + 1) });
      i = j + 1; continue;
    }
    // Word
    const wordM = line.slice(i).match(/^[a-zA-Z_]\w*/);
    if (wordM) {
      const w = wordM[0];
      let cls = 'tok-ident';
      if (kws.has(w))  cls = 'tok-keyword';
      if (typs.has(w)) cls = 'tok-type';
      tokens.push({ cls, text: w });
      i += w.length; continue;
    }
    // Number
    const numM = line.slice(i).match(/^\d+(?:\.\d+)?/);
    if (numM) {
      tokens.push({ cls: 'tok-number', text: numM[0] });
      i += numM[0].length; continue;
    }
    // Punctuation / operator
    tokens.push({ cls: 'tok-punct', text: line[i] });
    i++;
  }
  return tokens;
}

function renderCode(code, lang = 'app') {
  const lines = code.split('\n');
  return lines.map((line, idx) => {
    const toks = tokenizeLine(line, lang);
    const spans = toks.map(t => `<span class="${t.cls}">${escHtml(t.text)}</span>`).join('');
    return `<div class="code-line"><span class="line-num">${idx + 1}</span><span>${spans}</span></div>`;
  }).join('');
}

function escHtml(s) {
  return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
}

