// ── Install steps ─────────────────────────────────────────────────────────────

function renderInstall() {
  const container = document.getElementById('installSteps');
  container.innerHTML = INSTALL_STEPS.map((s, i) => {
    const lines = s.lines.map(l => {
      const isComment = l.startsWith('#');
      return `<span class="cmd-line"><span class="${isComment ? 'cmd-comment' : 'cmd-prefix'}">${isComment ? '' : '$ '}</span>${escHtml(l)}</span>`;
    }).join('');
    const copyText = s.lines.filter(l => !l.startsWith('#')).join('\n');
    return `
      <div class="install-step">
        <div class="step-num">${i + 1}</div>
        <div class="step-body">
          <div class="step-title">${s.title}</div>
          <div class="step-code-wrap">
            <pre class="step-code">${lines}</pre>
            <button class="copy-btn" data-copy="${escHtml(copyText)}">Copy</button>
          </div>
        </div>
      </div>
    `;
  }).join('');

  document.querySelectorAll('.copy-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      navigator.clipboard?.writeText(btn.dataset.copy).then(() => {
        btn.textContent = '✓ Copied';
        btn.classList.add('copied');
        setTimeout(() => { btn.textContent = 'Copy'; btn.classList.remove('copied'); }, 2000);
      });
    });
  });

  // Quick ref
  document.getElementById('quickrefGrid').innerHTML = QUICKREF.map(([cmd, desc]) =>
    `<div class="qr-item"><span class="qr-cmd">${escHtml(cmd)}</span><span class="qr-desc">${desc}</span></div>`
  ).join('');
}

renderInstall();

